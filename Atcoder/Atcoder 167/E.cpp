#define MOD 998244353

const int MAX = 510000;

long long fac[MAX], finv[MAX], inv[MAX];


void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}


long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

long long modpow(long long a, long long n, long long mod) {
    long long res = 1;
    while (n > 0) {
        if (n & 1) res = res * a % mod;
        a = a * a % mod;
        n >>= 1;
    }
    return res;
}



int main() {
    COMinit();
    ll N, M, K;
    cin >> N >> M >> K;
    ll ans = 0;
    ll tmp = M*modpow(M-1, N-K-1, MOD) % MOD;
    for (ll i = K; i >= 0; i--) {
        ans += tmp * COM(N-1, i) % MOD;
        ans %= MOD;
        tmp = (tmp*(M-1))%MOD;
    }
    cout << ans << endl;
}
