int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin>>t;
    while(t--){
        int N;
        cin >> N;
        vector<int> L(N), R(N);
        ordered_set<pair<int, int>> L_set, R_set;
    
        for (int i = 0; i < N; i++) {
            cin >> L[i] >> R[i];
            L_set.insert({L[i], i});
            R_set.insert({R[i], i});
        }
    
        int best = N;
    
        for (int i = 0; i < N; i++) {
            int current = int(R_set.order_of_key(make_pair(L[i], -1))) + (N - int(L_set.order_of_key(make_pair(R[i], N + 1))));
            best = min(best, current);
        }
    
        cout << best << '\n';
    }
    return 0;
}

